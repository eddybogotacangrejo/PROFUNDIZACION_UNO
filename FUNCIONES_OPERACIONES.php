<?php
class calcula
    {
    public function sumar($x,$y){
        return $x + $y;
    }
    public function restar($x,$y){
        return $x - $y;
    }   
    public function multiplicar($x,$y){
        return $x * $y;
    }  
    public function dividir($x,$y){
        return $x / $y;
    }         
}
?>